package assignmentDay3;

public class User {
	
	String id;
	String firstName;
	String lastName;
	String userName;
	String password;
	
	public User(String id, String firstName, String lastName, String userName, String password) {
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.userName = userName;
		this.password = password;
	} 
	
	

}
